-- Core inicial de integración y auditoría.
-- Ejecutar en el esquema definido para SIS-FACT.

CREATE TABLE SIS_DATA_SOURCE (
    DATA_SOURCE_ID        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SOURCE_CODE           VARCHAR2(50) NOT NULL UNIQUE,
    SOURCE_NAME           VARCHAR2(200) NOT NULL,
    SOURCE_TYPE           VARCHAR2(30) NOT NULL,
    SYSTEM_ROLE           VARCHAR2(50) NOT NULL,
    CONNECTION_KEY        VARCHAR2(100) NOT NULL,
    PRIORITY_ORDER        NUMBER DEFAULT 100 NOT NULL,
    ACTIVE_FLAG           CHAR(1) DEFAULT 'S' NOT NULL,
    HEALTH_STATUS         VARCHAR2(30),
    LAST_SUCCESS_AT       DATE,
    CREATED_AT            DATE DEFAULT SYSDATE NOT NULL,
    UPDATED_AT            DATE
);

CREATE TABLE SIS_EXTRACTION_RUN (
    EXTRACTION_ID         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DATA_SOURCE_ID        NUMBER NOT NULL REFERENCES SIS_DATA_SOURCE(DATA_SOURCE_ID),
    EXTRACTION_TYPE       VARCHAR2(80) NOT NULL,
    PERIOD_CODE           VARCHAR2(20),
    FLOW_CODE             VARCHAR2(50),
    SCOPE_CODE            VARCHAR2(50),
    EXECUTION_CODE        VARCHAR2(100),
    STATUS                VARCHAR2(30) DEFAULT 'CREATED' NOT NULL,
    ROWS_READ             NUMBER DEFAULT 0 NOT NULL,
    ROWS_LOADED           NUMBER DEFAULT 0 NOT NULL,
    ROWS_REJECTED         NUMBER DEFAULT 0 NOT NULL,
    ERROR_MESSAGE         VARCHAR2(4000),
    QUERY_VERSION         VARCHAR2(50),
    STARTED_AT            DATE DEFAULT SYSDATE NOT NULL,
    FINISHED_AT           DATE
);

CREATE TABLE SIS_INTEGRATION_CALL (
    INTEGRATION_CALL_ID   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DATA_SOURCE_ID        NUMBER REFERENCES SIS_DATA_SOURCE(DATA_SOURCE_ID),
    ENDPOINT_NAME         VARCHAR2(120),
    EXECUTION_CODE        VARCHAR2(100),
    CORRELATION_ID        VARCHAR2(120),
    REQUEST_TIMESTAMP     DATE DEFAULT SYSDATE NOT NULL,
    RESPONSE_TIMESTAMP    DATE,
    RESPONSE_TIME_MS      NUMBER,
    TECHNICAL_STATUS      VARCHAR2(50),
    BUSINESS_STATUS       VARCHAR2(50),
    HTTP_STATUS           NUMBER,
    RETRY_NUMBER          NUMBER DEFAULT 0,
    REQUEST_HASH          VARCHAR2(200),
    RESPONSE_CODE         VARCHAR2(100),
    ERROR_MESSAGE         VARCHAR2(4000)
);

CREATE TABLE SIS_AUDIT_LOG (
    AUDIT_ID              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ACTION_CODE           VARCHAR2(100) NOT NULL,
    USER_ID               VARCHAR2(100),
    MODULE_CODE           VARCHAR2(80) NOT NULL,
    ENTITY_TYPE           VARCHAR2(80),
    ENTITY_ID             VARCHAR2(120),
    METADATA_JSON         CLOB,
    CREATED_AT            DATE DEFAULT SYSDATE NOT NULL
);

CREATE TABLE SIS_AI_PROVIDER (
    AI_PROVIDER_ID        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PROVIDER_CODE         VARCHAR2(50) NOT NULL UNIQUE,
    PROVIDER_NAME         VARCHAR2(200) NOT NULL,
    PROVIDER_TYPE         VARCHAR2(30) NOT NULL,
    BASE_URL              VARCHAR2(500),
    MODEL_NAME            VARCHAR2(200),
    ACTIVE_FLAG           CHAR(1) DEFAULT 'N' NOT NULL,
    CREATED_AT            DATE DEFAULT SYSDATE NOT NULL,
    UPDATED_AT            DATE
);

CREATE INDEX IX_SIS_EXTRACTION_01 ON SIS_EXTRACTION_RUN(DATA_SOURCE_ID, PERIOD_CODE, STATUS);
CREATE INDEX IX_SIS_CALL_01 ON SIS_INTEGRATION_CALL(DATA_SOURCE_ID, CORRELATION_ID);
CREATE INDEX IX_SIS_AUDIT_01 ON SIS_AUDIT_LOG(MODULE_CODE, CREATED_AT);
